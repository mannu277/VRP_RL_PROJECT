def build_training_samples_onpolicy(coords, tt, P_dummy,
                                    relu_model, vfa_scale,
                                    n_samples=TRAIN_SAMPLES,
                                    service_time=1.0,
                                    seed=123):
    random.seed(seed)
    np.random.seed(seed)
    vehicles = make_initial_vehicles(coords)
    st = State(10.0, 0.0, tt, coords, vehicles)
    samples = []
    pending: List[Request] = []
    dt = 2.0

    while len(samples) < n_samples:
        n_new = np.random.poisson(ARRIVAL_RATE * dt)
        for _ in range(n_new):
            req = generate_request(
                st,
                slack_low=BASE_SLACK_LOW * DEMAND_MULT,
                slack_high=BASE_SLACK_HIGH * DEMAND_MULT,
                service_time=service_time
            )
            pending.append(req)
        pending = [r for r in pending if r.deadline >= st.t]
        if not pending:
            st.t += dt
            continue

        a, obj, served_idx = plan_epoch_with_relu_vfa(
            st, pending, P_dummy, relu_model,
            lambda_vfa=LAMBDA_VFA, vfa_scale=vfa_scale,
            service_time=service_time, timelimit=TLIM_PLAN
        )

        z, Bmin_res, Bmax_res, Bbar_res, RT_res = aggregate_features_residual(
            st, a, P_dummy, service_time
        )
        x = np.array([
            z[0],
            min(1.0, Bmin_res / st.T),
            min(1.0, Bmax_res / st.T),
            min(1.0, Bbar_res / st.T),
            min(1.0, RT_res   / st.T),
        ], dtype=float)

        st_next = State(st.T, st.t, st.tt, st.coords,
                        [Vehicle(v.id, v.capacity, v.time, v.node, v.load)
                         for v in st.vehicles])
        st_next = apply_action(st_next, a, service_time)
        st_next.t = st.t + dt

        y = estimate_return_rollout(
            coords, tt, st_next, P_dummy,
            H=ROLLOUT_H, dt=dt,
            service_time=service_time,
            slack_low=BASE_SLACK_LOW,
            slack_high=BASE_SLACK_HIGH
        )

        samples.append((x, y))

        for idx in sorted(served_idx, reverse=True):
            pending.pop(idx)
        st = apply_action(st, a, service_time)
        st.t += dt

    return samples

def calibrate_scale(relu: ReLUVFA, samples, target=CALIB_TARGET):
    outs = [abs(relu.predict(x)) for (x, _) in samples] if samples else [1.0]
    m = float(np.median(outs)) if outs else 1.0
    return 1.0 if m < 1e-9 else float(target / m)

def _train_once(samples: List[Tuple[np.ndarray, float]],
                hidden: int, w_bound: float, timelimit: float):
    d = 5
    h = hidden
    m = gp.Model("relu_fit", env=GLOBAL_ENV)
    m.Params.OutputFlag = 0
    m.Params.TimeLimit = timelimit
    W1 = m.addVars(h, d, lb=-w_bound, ub=w_bound, name="W1")
    b1 = m.addVars(h,    lb=-w_bound, ub=w_bound, name="b1")
    W2 = m.addVars(h,    lb=-w_bound, ub=w_bound, name="W2")
    b2 = m.addVar(lb=-w_bound, ub=w_bound, name="b2")
    terms = []
    for n, (x, y) in enumerate(samples):
        x = np.asarray(x, dtype=float).tolist()
        sumx = sum(abs(v) for v in x)
        L = -w_bound*sumx - w_bound
        U =  w_bound*sumx + w_bound
        s   = m.addVars(h, lb=L, ub=U, name=f"s_{n}")
        yhi = m.addVars(h, lb=0.0, name=f"y_{n}")
        z   = m.addVars(h, vtype=GRB.BINARY, name=f"z_{n}")
        for j in range(h):
            m.addConstr(
                s[j] == gp.quicksum(W1[j, i]*x[i] for i in range(d)) + b1[j],
                name=f"sdef_{n}_{j}"
            )
            m.addConstr(yhi[j] >= s[j], name=f"relu_ge_{n}_{j}")
            m.addConstr(yhi[j] <= s[j] - L*(1 - z[j]), name=f"relu_on_{n}_{j}")
            m.addConstr(yhi[j] <= U * z[j], name=f"relu_off_{n}_{j}")
        yhat = m.addVar(lb=-GRB.INFINITY, name=f"yhat_{n}")
        m.addConstr(
            yhat == gp.quicksum(W2[j]*yhi[j] for j in range(h)) + b2,
            name=f"out_{n}"
        )
        ep = m.addVar(lb=0.0, name=f"ep_{n}")
        en = m.addVar(lb=0.0, name=f"en_{n}")
        m.addConstr(yhat - float(y) == ep - en, name=f"abs_{n}")
        terms.append(ep + en)
    m.setObjective(gp.quicksum(terms), GRB.MINIMIZE)
    m.optimize()
    W1_np = np.array([[W1[j, i].X for i in range(d)] for j in range(h)], dtype=float)
    b1_np = np.array([b1[j].X for j in range(h)], dtype=float)
    W2_np = np.array([W2[j].X for j in range(h)], dtype=float)
    b2_sc = float(b2.X)
    return W1_np, b1_np, W2_np, b2_sc

def train_relu_vfa_in_milp(samples: List[Tuple[np.ndarray, float]],
                           hidden=RELU_HIDDEN,
                           w_bound=W_BOUND,
                           timelimit=TLIM_TRAIN):
    for n_try in TRAIN_LIMITS:
        use = samples[:min(n_try, len(samples))]
        try:
            return _train_once(use, hidden, w_bound, timelimit)
        except GurobiError as e:
            if "size-limited license" in str(e).lower():
                continue
            else:
                raise
    for h_try in [max(2, hidden//2), 2]:
        for n_try in TRAIN_LIMITS[::-1]:
            use = samples[:min(n_try, len(samples))]
            try:
                return _train_once(use, h_try, w_bound, timelimit)
            except GurobiError as e:
                if "size-limited license" in str(e).lower():
                    continue
                else:
                    raise
    raise RuntimeError("Training MILP too large for license.")

def run_episode(state: State, P_dummy, relu_model: ReLUVFA,
                lam=LAMBDA_VFA, vfa_scale=1.0,
                steps=40, dt=2.0, service_time=1.0,
                max_pending=MAX_PENDING):
    accepted = 0
    pending: List[Request] = []
    for _ in range(steps):
        n_new = np.random.poisson(ARRIVAL_RATE * dt)
        for _ in range(n_new):
            req = generate_request(
                state,
                slack_low=BASE_SLACK_LOW * DEMAND_MULT,
                slack_high=BASE_SLACK_HIGH * DEMAND_MULT,
                service_time=service_time
            )
            pending.append(req)
        pending = [r for r in pending if r.deadline >= state.t]
        if len(pending) > max_pending:
            pending = pending[-max_pending:]
        if not pending:
            state.t += dt
            continue
        a, obj, served_idx = plan_epoch_with_relu_vfa(
            state, pending, P_dummy, relu_model, lam, vfa_scale,
            service_time, TLIM_PLAN
        )
        accepted += len(served_idx)
        state = apply_action(state, a, service_time)
        for idx in sorted(served_idx, reverse=True):
            pending.pop(idx)
        state.t += dt
    return accepted

def set_all_seeds(s):
    random.seed(s)
    np.random.seed(s)
    torch.manual_seed(s)

def run_heuristic_episode(coords, tt, P_dummy, steps=40,
                          service_time=1.0):
    vehicles = make_initial_vehicles(coords)
    st = State(10.0, 0.0, tt, coords, vehicles)
    acc = 0
    pending: List[Request] = []
    dt = 2.0
    for _ in range(steps):
        n_new = np.random.poisson(ARRIVAL_RATE * dt)
        for _ in range(n_new):
            r = generate_request(
                st,
                slack_low=BASE_SLACK_LOW * DEMAND_MULT,
                slack_high=BASE_SLACK_HIGH * DEMAND_MULT,
                service_time=service_time
            )
            pending.append(r)
        pending = [r for r in pending if r.deadline >= st.t]
        if not pending:
            st.t += dt
            continue
        req = pending[-1]
        a, _ = pick_best_action_heur(st, req, P_dummy, service_time)
        acc += int(immediate_reward(a))
        st = apply_action(st, a, service_time)
        if immediate_reward(a) > 0:
            pending.pop()
        st.t += dt
    return acc

def run_milp_vfa_episode(coords, tt, P_dummy, relu_model, vfa_scale,
                         lam, steps=40, service_time=1.0):
    vehicles = make_initial_vehicles(coords)
    st = State(10.0, 0.0, tt, coords, vehicles)
    return run_episode(st, P_dummy, relu_model,
                       lam=lam, vfa_scale=vfa_scale,
                       steps=steps, service_time=service_time)

