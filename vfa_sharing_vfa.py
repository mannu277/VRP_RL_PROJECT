def make_initial_vehicles(coords):
    if N_VEHICLES == 2:
        return [
            Vehicle(0, 2, 0.0, 0),
            Vehicle(1, 2, 0.0, len(coords) - 1),
        ]
    elif N_VEHICLES == 3:
        center = len(coords) // 2
        return [
            Vehicle(0, 2, 0.0, 0),
            Vehicle(1, 2, 0.0, center),
            Vehicle(2, 2, 0.0, len(coords) - 1),
        ]
    else:
        vehicles = []
        n_nodes = len(coords)
        for vid in range(N_VEHICLES):
            node = int(vid * (n_nodes - 1) / max(1, N_VEHICLES - 1))
            vehicles.append(Vehicle(vid, 2, 0.0, node))
        return vehicles

def make_grid(n_side: int, area: float):
    coords = []
    step = area / (n_side - 1) if n_side > 1 else 0.0
    for r in range(n_side):
        for c in range(n_side):
            coords.append((r*step, c*step))
    N = n_side * n_side
    tt = np.zeros((N, N), dtype=float)
    for i, (xi, yi) in enumerate(coords):
        for j, (xj, yj) in enumerate(coords):
            tt[i, j] = 0.0 if i == j else ((xi - xj)**2 + (yi - yj)**2) ** 0.5
    return coords, tt

def make_dummy_points(k_side: int, area: float):
    pts = []
    step = area / (k_side + 1)
    for r in range(1, k_side + 1):
        for c in range(1, k_side + 1):
            pts.append((r*step, c*step))
    return pts

def _dist_node_to_point(coords, j: NodeId, pt: Tuple[float, float]) -> float:
    xj, yj = coords[j]
    x, y = pt
    return hypot(xj - x, yj - y)

def generate_request(state: State, pax_min=1, pax_max=2,
                     slack_low=0.5, slack_high=2.0, service_time=1.0) -> Request:
    N = len(state.coords)
    p = random.randrange(N)
    d = random.randrange(N)
    while d == p:
        d = random.randrange(N)
    pax = random.randint(pax_min, pax_max)
    travel_pd = state.tt[p, d]
    deadline = state.t + travel_pd + 2*service_time + random.uniform(slack_low, slack_high)
    return Request(pickup=p, drop=d, pax=pax, deadline=deadline)

def enumerate_actions(state: State, req: Request, service_time: float = 1.0) -> List[Action]:
    cands = [{}]
    for v in state.vehicles:
        if v.load + req.pax > v.capacity:
            continue
        arr_pick = v.time + state.tt[v.node, req.pickup] + service_time
        arr_drop = arr_pick + state.tt[req.pickup, req.drop] + service_time
        if arr_drop <= req.deadline:
            cands.append({v.id: [(v.node, req.pickup), (req.pickup, req.drop)]})
    return cands

def immediate_reward(a: Action) -> float:
    return 0.0 if not a else 1.0

def apply_action(state: State, a: Action, service_time: float = 1.0) -> State:
    if not a:
        return state
    for v_id, route in a.items():
        v = next(vh for vh in state.vehicles if vh.id == v_id)
        t = v.time
        node = v.node
        for (i, j) in route:
            t += state.tt[i, j] + service_time
            node = j
        v.time = t
        v.node = node
    return state

def finish_times_and_last_nodes(state: State, action: Action, service_time: float = 1.0):
    B_v = []
    last_nodes = []
    for v in state.vehicles:
        t = v.time
        node = v.node
        for (i, j) in action.get(v.id, []):
            t += state.tt[i, j] + service_time
            node = j
        B_v.append(t)
        last_nodes.append(node)
    return np.array(B_v, dtype=float), last_nodes

def response_time_RT_abs(state: State, B_v: np.ndarray,
                         last_nodes: List[NodeId],
                         dummy_points: List[Tuple[float, float]]) -> float:
    worst = 0.0
    for pt in dummy_points:
        best_here = min(B_v[idx] + _dist_node_to_point(state.coords, last_nodes[idx], pt)
                        for idx in range(len(B_v)))
        worst = max(worst, best_here)
    return worst

def aggregate_features_residual(state: State, action: Action,
                                dummy_points: List[Tuple[float, float]],
                                service_time: float = 1.0):
    T = max(state.T, 1e-6)
    B_v_abs, last_nodes = finish_times_and_last_nodes(state, action, service_time)
    if len(B_v_abs) == 0:
        Bmin_res = Bmax_res = Bbar_res = RT_res = 0.0
    else:
        B_res = np.maximum(0.0, B_v_abs - state.t)
        Bmin_res = float(B_res.min())
        Bmax_res = float(B_res.max())
        Bbar_res = float(B_res.mean())
        RT_abs = response_time_RT_abs(state, B_v_abs, last_nodes, dummy_points)
        RT_res = max(0.0, RT_abs - state.t)
    t_phase = (state.t % T) / T
    z = np.array([
        t_phase,
        min(1.0, Bmin_res / T),
        min(1.0, Bmax_res / T),
        min(1.0, Bbar_res / T),
        min(1.0, RT_res   / T)
    ], dtype=np.float32)
    return z, Bmin_res, Bmax_res, Bbar_res, RT_res

def pick_best_action_heur(state: State, req: Request, P,
                          service_time=1.0):
    best_a = {}
    best_s = -1e9
    best_rt = 1e9
    for a in enumerate_actions(state, req, service_time):
        s = immediate_reward(a)
        _, _, _, _, RT_res = aggregate_features_residual(state, a, P, service_time)
        if (s > best_s) or (s == best_s and RT_res < best_rt):
            best_a = a
            best_s = s
            best_rt = RT_res
    return best_a, best_s

class ReLUVFA:
    def __init__(self, hidden=RELU_HIDDEN, device="cpu"):
        self.model = nn.Sequential(
            nn.Linear(5, hidden),
            nn.ReLU(),
            nn.Linear(hidden, 1)
        ).to(device)
        with torch.no_grad():
            for p in self.model.parameters():
                p.zero_()

    def predict(self, x: np.ndarray) -> float:
        with torch.no_grad():
            t = torch.tensor(x, dtype=torch.float32).unsqueeze(0)
            return float(self.model(t).squeeze())

def extract_relu_weights(m: "ReLUVFA"):
    l1, _, l2 = m.model
    W1 = l1.weight.detach().cpu().numpy()
    b1 = l1.bias.detach().cpu().numpy()
    W2 = l2.weight.detach().cpu().numpy()
    b2 = l2.bias.detach().cpu().numpy()
    return W1, b1, W2.reshape(-1), float(b2.item())

def add_min_over(m, vals, M, name):
    n = len(vals)
    r = m.addVar(lb=-GRB.INFINITY, name=f"{name}_min")
    z = m.addVars(n, vtype=GRB.BINARY, name=f"{name}_zmin")
    for i in range(n):
        m.addConstr(r <= vals[i], name=f"{name}_min_le_{i}")
        m.addConstr(r >= vals[i] - M*(1 - z[i]), name=f"{name}_min_ge_{i}")
    m.addConstr(gp.quicksum(z[i] for i in range(n)) == 1, name=f"{name}_pick1")
    return r

def add_max_over(m, vals, M, name):
    n = len(vals)
    r = m.addVar(lb=-GRB.INFINITY, name=f"{name}_max")
    z = m.addVars(n, vtype=GRB.BINARY, name=f"{name}_zmax")
    for i in range(n):
        m.addConstr(r >= vals[i], name=f"{name}_max_ge_{i}")
        m.addConstr(r <= vals[i] + M*(1 - z[i]), name=f"{name}_max_le_{i}")
    m.addConstr(gp.quicksum(z[i] for i in range(n)) == 1, name=f"{name}_pick1")
    return r

def bounds_affine_row(w_row: np.ndarray, b: float, xL: np.ndarray, xU: np.ndarray):
    lo = b
    hi = b
    for wj, lj, uj in zip(w_row, xL, xU):
        if wj >= 0:
            lo += wj * lj
            hi += wj * uj
        else:
            lo += wj * uj
            hi += wj * lj
    return lo, hi

def add_relu_block_34_38(m, x_vars, W1, b1, W2, b2, xL, xU, name="vfa"):
    d = len(x_vars)
    h = W1.shape[0]
    s = m.addVars(h, lb=-GRB.INFINITY, name=f"{name}_s")
    y = m.addVars(h, lb=0.0, name=f"{name}_y")
    z = m.addVars(h, vtype=GRB.BINARY, name=f"{name}_z")
    for j in range(h):
        L, U = bounds_affine_row(W1[j], b1[j], np.array(xL), np.array(xU))
        if U < L:
            L, U = U, L
        if abs(U - L) < 1e-6:
            U += 1e-6
        m.addConstr(
            s[j] == gp.quicksum(float(W1[j, i]) * x_vars[i] for i in range(d)) + float(b1[j]),
            name=f"{name}_aff_{j}"
        )
        m.addConstr(y[j] >= s[j], name=f"{name}_y_ge_s_{j}")
        m.addConstr(y[j] <= s[j] - L*(1 - z[j]), name=f"{name}_relu_on_{j}")
        m.addConstr(y[j] <= U * z[j], name=f"{name}_relu_off_{j}")
    vhat = m.addVar(lb=-GRB.INFINITY, name=f"{name}_out")
    m.addConstr(
        vhat == gp.quicksum(float(W2[j]) * y[j] for j in range(h)) + float(b2),
        name=f"{name}_out_def"
    )
    return vhat

def enumerate_vehicle_plans(state: State, pending: List[Request], v_idx: int,
                            service_time: float = 1.0):
    v = state.vehicles[v_idx]
    tt = state.tt
    plans = []

    plans.append({
        "served_idx": [],
        "route": [],
        "B": v.time,
        "last_node": v.node,
    })

    Nreq = len(pending)
    if Nreq == 0:
        return plans

    def simulate_sequence(events):
        t = v.time
        node = v.node
        load = 0
        cap = v.capacity
        for etype, k in events:
            req = pending[k]
            if etype == "pickup":
                t += tt[node, req.pickup] + service_time
                load += req.pax
                if load > cap:
                    return False, None, None
                node = req.pickup
            else:
                t += tt[node, req.drop] + service_time
                load -= req.pax
                if t > req.deadline:
                    return False, None, None
                node = req.drop
        return True, t, node

    for k in range(Nreq):
        req = pending[k]
        arr_pick = v.time + tt[v.node, req.pickup] + service_time
        arr_drop = arr_pick + tt[req.pickup, req.drop] + service_time
        if arr_drop <= req.deadline and req.pax <= v.capacity:
            ok, B, last_node = simulate_sequence([
                ("pickup", k),
                ("drop",   k),
            ])
            if ok:
                plans.append({
                    "served_idx": [k],
                    "route": [
                        (v.node, req.pickup),
                        (req.pickup, req.drop),
                    ],
                    "B": B,
                    "last_node": last_node,
                })

    for k1, k2 in combinations(range(Nreq), 2):
        r1, r2 = pending[k1], pending[k2]
        if r1.pax + r2.pax > v.capacity:
            continue
        events = [
            ("pickup", k1),
            ("drop",   k1),
            ("pickup", k2),
            ("drop",   k2),
        ]
        for perm in set(permutations(events, 4)):
            seen_pick1 = False
            seen_pick2 = False
            valid = True
            for etype, k in perm:
                if k == k1:
                    if etype == "pickup":
                        seen_pick1 = True
                    else:
                        if not seen_pick1:
                            valid = False
                            break
                else:
                    if etype == "pickup":
                        seen_pick2 = True
                    else:
                        if not seen_pick2:
                            valid = False
                            break
            if not valid:
                continue
            ok, B, last_node = simulate_sequence(perm)
            if not ok:
                continue
            route = []
            cur_node = v.node
            for etype, k in perm:
                req = pending[k]
                target = req.pickup if etype == "pickup" else req.drop
                route.append((cur_node, target))
                cur_node = target
            plans.append({
                "served_idx": sorted([k1, k2]),
                "route": route,
                "B": B,
                "last_node": last_node,
            })

    return plans

def plan_epoch_with_relu_vfa(state: State, pending: List[Request], P_dummy: List[Tuple[float,float]],
                             relu_model: ReLUVFA, lambda_vfa=LAMBDA_VFA, vfa_scale=1.0,
                             service_time=1.0, timelimit=TLIM_PLAN):
    Vset = list(range(len(state.vehicles)))
    Kset = list(range(len(pending)))
    T = max(state.T, 1e-6)
    tt = state.tt

    vehicle_plans = []
    for v in Vset:
        plans_v = enumerate_vehicle_plans(state, pending, v, service_time=service_time)
        if not plans_v:
            plans_v = [{
                "served_idx": [],
                "route": [],
                "B": state.vehicles[v].time,
                "last_node": state.vehicles[v].node,
            }]
        vehicle_plans.append(plans_v)

    max_tt = float(np.max(tt) if tt.size else 0.0)
    M = float(T + max_tt + 100.0)

    m = gp.Model("epoch_vfa_rideshare", env=GLOBAL_ENV)
    m.Params.OutputFlag = 0
    m.Params.TimeLimit  = timelimit
    m.Params.MIPFocus   = 1
    m.Params.Heuristics = 0.2

    y = {}
    for v in Vset:
        for p in range(len(vehicle_plans[v])):
            y[(v, p)] = m.addVar(vtype=GRB.BINARY, name=f"y_{v}_{p}")

    for v in Vset:
        m.addConstr(
            gp.quicksum(y[(v, p)] for p in range(len(vehicle_plans[v]))) == 1,
            name=f"veh_one_plan_{v}"
        )

    for k in Kset:
        terms = []
        for v in Vset:
            for p, plan in enumerate(vehicle_plans[v]):
                if k in plan["served_idx"]:
                    terms.append(y[(v, p)])
        if terms:
            m.addConstr(gp.quicksum(terms) <= 1, name=f"req_once_{k}")

    B_v = m.addVars(Vset, lb=0.0, name="B_v")
    for v in Vset:
        m.addConstr(
            B_v[v] == gp.quicksum(vehicle_plans[v][p]["B"] * y[(v, p)]
                                  for p in range(len(vehicle_plans[v]))),
            name=f"Bv_def_{v}"
        )

    R_v = m.addVars(Vset, lb=0.0, name="R_v")
    for v in Vset:
        m.addConstr(R_v[v] >= B_v[v] - state.t, name=f"res_{v}")

    B_minus = add_min_over(m, [R_v[v] for v in Vset], M, name="Bminus")
    B_plus  = add_max_over(m, [R_v[v] for v in Vset], M, name="Bplus")

    B_bar = m.addVar(lb=0.0, name="Bbar")
    if len(Vset) > 0:
        m.addConstr(
            B_bar == (1.0 / len(Vset)) * gp.quicksum(R_v[v] for v in Vset),
            name="Bbar_def"
        )

    r_iv_abs = {}
    for i, pt in enumerate(P_dummy):
        for v in Vset:
            r_iv_abs[(i, v)] = m.addVar(lb=0.0, name=f"rabs_{i}_{v}")
            expr = gp.quicksum(
                (vehicle_plans[v][p]["B"] +
                 _dist_node_to_point(state.coords, vehicle_plans[v][p]["last_node"], pt)) *
                y[(v, p)]
                for p in range(len(vehicle_plans[v]))
            )
            m.addConstr(r_iv_abs[(i, v)] == expr, name=f"rabs_def_{i}_{v}")

    r_i_abs = {}
    for i in range(len(P_dummy)):
        r_i_abs[i] = add_min_over(
            m, [r_iv_abs[(i, v)] for v in Vset], M, name=f"rtmin_i{i}"
        )

    RT_abs = add_max_over(
        m, [r_i_abs[i] for i in range(len(P_dummy))], M, name="RT_abs"
    )

    RT_res = m.addVar(lb=0.0, name="RT_res")
    m.addConstr(RT_res >= RT_abs - state.t, name="RT_res_def")

    t_phase = (state.t % T) / T
    x_vars = [m.addVar(lb=0.0, ub=1.0, name=n)
              for n in ["x_t", "x_bmin", "x_bmax", "x_bbar", "x_rt"]]

    m.addConstr(x_vars[0] == t_phase,              name="x_t_eq")
    m.addConstr(x_vars[1] == B_minus / T,          name="x_bmin_eq")
    m.addConstr(x_vars[2] == B_plus  / T,          name="x_bmax_eq")
    m.addConstr(x_vars[3] == B_bar   / T,          name="x_bbar_eq")
    m.addConstr(x_vars[4] == RT_res  / T,          name="x_rt_eq")

    W1, b1, W2, b2 = extract_relu_weights(relu_model)
    vhat = add_relu_block_34_38(
        m, x_vars, W1, b1, W2, b2,
        xL=[0.0]*5, xU=[1.0]*5,
        name="vfa"
    )

    served = gp.quicksum(
        len(vehicle_plans[v][p]["served_idx"]) * y[(v, p)]
        for v in Vset for p in range(len(vehicle_plans[v]))
    )

    m.setObjective(served + float(lambda_vfa * vfa_scale) * vhat, GRB.MAXIMIZE)
    m.optimize()

    action: Action = {}
    served_idx = []

    for v in Vset:
        chosen_p = None
        for p in range(len(vehicle_plans[v])):
            if y[(v, p)].X > 0.5:
                chosen_p = p
                break
        if chosen_p is None:
            continue
        plan = vehicle_plans[v][chosen_p]
        if plan["served_idx"]:
            action[v] = list(plan["route"])
            served_idx.extend(plan["served_idx"])

    served_idx = sorted(set(served_idx))
    return action, float(m.ObjVal), served_idx

def estimate_return_rollout(coords, tt, base_state: State, P_dummy,
                            H=ROLLOUT_H, dt=2.0, service_time=1.0,
                            slack_low=0.5, slack_high=2.0):
    st = State(base_state.T, base_state.t, tt, coords,
               [Vehicle(v.id, v.capacity, v.time, v.node, v.load)
                for v in base_state.vehicles])
    pending: List[Request] = []
    ret = 0.0
    disc = 1.0
    for _ in range(H):
        n_new = np.random.poisson(ARRIVAL_RATE * dt)
        for _ in range(n_new):
            req = generate_request(st, slack_low=slack_low,
                                   slack_high=slack_high,
                                   service_time=service_time)
            pending.append(req)
        pending = [r for r in pending if r.deadline >= st.t]
        if not pending:
            st.t += dt
            disc *= GAMMA
            continue
        req = pending[-1]
        a, _ = pick_best_action_heur(st, req, P_dummy, service_time)
        r = immediate_reward(a)
        st = apply_action(st, a, service_time)
        if r > 0:
            pending.pop()
        ret += disc * r
        disc *= GAMMA
        st.t += dt
    return ret

def build_training_samples(coords, tt, P_dummy,
                           n_samples=TRAIN_SAMPLES, service_time=1.0,
                           seed=123):
    random.seed(seed)
    np.random.seed(seed)
    vehicles = make_initial_vehicles(coords)
    st = State(10.0, 0.0, tt, coords, vehicles)
    samples = []
    pending: List[Request] = []
    dt = 2.0
    while len(samples) < n_samples:
        n_new = np.random.poisson(ARRIVAL_RATE * dt)
        for _ in range(n_new):
            req = generate_request(st, slack_low=0.5, slack_high=2.0,
                                   service_time=service_time)
            pending.append(req)
        pending = [r for r in pending if r.deadline >= st.t]
        if not pending:
            st.t += dt
            continue
        req = pending[-1]
        a, _ = pick_best_action_heur(st, req, P_dummy, service_time)
        z, Bmin_res, Bmax_res, Bbar_res, RT_res = aggregate_features_residual(st, a, P_dummy, service_time)
        x = np.array([
            z[0],
            min(1.0, Bmin_res/st.T),
            min(1.0, Bmax_res/st.T),
            min(1.0, Bbar_res/st.T),
            min(1.0, RT_res/st.T)
        ], dtype=float)
        st_next = State(st.T, st.t, st.tt, st.coords,
                        [Vehicle(v.id, v.capacity, v.time, v.node, v.load)
                         for v in st.vehicles])
        st_next = apply_action(st_next, a, service_time)
        st_next.t = st.t + dt
        y = estimate_return_rollout(coords, tt, st_next, P_dummy,
                                    H=ROLLOUT_H, dt=dt,
                                    service_time=service_time,
                                    slack_low=0.5, slack_high=2.0)
        samples.append((x, y))
        if immediate_reward(a) > 0:
            pending.pop()
        st.t += dt
    return samples

