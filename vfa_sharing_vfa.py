def train_vfa_over_k(coords, tt, base_state: State, P_dummy, K=JOINT_K,
                     hidden=RELU_HIDDEN, w_bound=W_BOUND_JOINT,
                     timelimit=TLIM_TRAIN, dt=2.0, service_time=1.0,
                     lam=LAMBDA_VFA_JOINT, gamma=GAMMA):
    V = len(base_state.vehicles)
    T = max(base_state.T, 1e-6)
    m = gp.Model("jointK", env=GLOBAL_ENV)
    m.Params.OutputFlag = 0
    m.Params.TimeLimit  = timelimit
    m.Params.MIPFocus   = 1
    m.Params.Heuristics = 0.2

    st_tmp = State(base_state.T, base_state.t, tt, coords,
                   [Vehicle(v.id, v.capacity, v.time, v.node, v.load)
                    for v in base_state.vehicles])
    reqs = []
    for k in range(K):
        r = generate_request(st_tmp, slack_low=0.5, slack_high=2.0,
                             service_time=service_time)
        reqs.append(r)
        st_tmp.t += dt

    x = m.addVars(V, K, vtype=GRB.BINARY, name="x")
    B = m.addVars(V, K, lb=0.0, name="B")
    R = m.addVars(V, K, lb=0.0, name="R")

    W1 = m.addVars(hidden, 5, lb=-w_bound, ub=w_bound, name="W1")
    b1 = m.addVars(hidden,    lb=-w_bound, ub=w_bound, name="b1")
    W2 = m.addVars(hidden,    lb=-w_bound, ub=w_bound, name="W2")
    b2 = m.addVar(lb=-w_bound, ub=w_bound, name="b2")

    v_node0 = [base_state.vehicles[v].node for v in range(V)]
    v_time0 = [base_state.vehicles[v].time for v in range(V)]
    v_cap   = [base_state.vehicles[v].capacity for v in range(V)]

    M = float(base_state.T + (np.max(tt) if tt.size else 0.0) + 100.0)

    vhat_vars = []
    RTres_vars = []

    for k in range(K):
        m.addConstr(gp.quicksum(x[v, k] for v in range(V)) <= 1,
                    name=f"assign_once_{k}")

        for v in range(V):
            m.addConstr(reqs[k].pax * x[v, k] <= v_cap[v],
                        name=f"cap_{v}_{k}")

        for v in range(V):
            travel = tt[v_node0[v], reqs[k].pickup] + service_time + \
                     tt[reqs[k].pickup, reqs[k].drop] + service_time

            if k == 0:
                m.addConstr(
                    B[v, k] == v_time0[v] + travel * x[v, k],
                    name=f"B_{v}_{k}"
                )
            else:
                m.addConstr(B[v, k] >= B[v, k-1], name=f"Bprop_ge_{v}_{k}")
                m.addConstr(
                    B[v, k] >= B[v, k-1] + travel * x[v, k],
                    name=f"Bprop_add_{v}_{k}"
                )

            m.addConstr(
                B[v, k] <= reqs[k].deadline + M*(1 - x[v, k]),
                name=f"ddl_{v}_{k}"
            )

        t_k = base_state.t + k*dt
        for v in range(V):
            m.addConstr(R[v, k] >= B[v, k] - t_k, name=f"res_{v}_{k}")

        Bminus = add_min_over(m, [R[v, k] for v in range(V)], M,
                              name=f"Bminus_k{k}")
        Bplus  = add_max_over(m, [R[v, k] for v in range(V)], M,
                              name=f"Bplus_k{k}")
        Bbar   = m.addVar(lb=0.0, name=f"Bbar_k{k}")
        m.addConstr(
            Bbar == (1.0 / V) * gp.quicksum(R[v, k] for v in range(V)),
            name=f"Bbar_def_k{k}"
        )

        r_iv_abs = {}
        for i in range(len(P_dummy)):
            for v in range(V):
                d_idle = _dist_node_to_point(coords, v_node0[v], P_dummy[i])
                d_switch = d_idle + x[v, k] * (
                    _dist_node_to_point(coords, reqs[k].drop, P_dummy[i]) - d_idle
                )
                r_iv_abs[(i, v)] = m.addVar(lb=0.0,
                                            name=f"rabs_{i}_{v}_k{k}")
                m.addConstr(
                    r_iv_abs[(i, v)] == B[v, k] + d_switch,
                    name=f"rabs_def_{i}_{v}_k{k}"
                )

        r_i_abs = {
            i: add_min_over(m, [r_iv_abs[(i, v)] for v in range(V)], M,
                            name=f"rtmin_i{i}_k{k}")
            for i in range(len(P_dummy))
        }
        RT_abs = add_max_over(
            m, [r_i_abs[i] for i in range(len(P_dummy))],
            M, name=f"RTabs_k{k}"
        )
        RT_res = m.addVar(lb=0.0, name=f"RTres_k{k}")
        m.addConstr(RT_res >= RT_abs - t_k, name=f"RTres_def_k{k}")
        RTres_vars.append(RT_res)

        t_phase = (t_k % T) / T
        x_t    = m.addVar(lb=0.0, ub=1.0, name=f"x_t_k{k}")
        x_bmin = m.addVar(lb=0.0, ub=1.0, name=f"x_bmin_k{k}")
        x_bmax = m.addVar(lb=0.0, ub=1.0, name=f"x_bmax_k{k}")
        x_bbar = m.addVar(lb=0.0, ub=1.0, name=f"x_bbar_k{k}")
        x_rt   = m.addVar(lb=0.0, ub=1.0, name=f"x_rt_k{k}")

        m.addConstr(x_t    == t_phase,     name=f"x_t_eq_k{k}")
        m.addConstr(x_bmin == Bminus/T,    name=f"x_bmin_eq_k{k}")
        m.addConstr(x_bmax == Bplus/T,     name=f"x_bmax_eq_k{k}")
        m.addConstr(x_bbar == Bbar/T,      name=f"x_bbar_eq_k{k}")
        m.addConstr(x_rt   == RT_res/T,    name=f"x_rt_eq_k{k}")

        s = m.addVars(hidden, lb=-GRB.INFINITY, name=f"s_k{k}")
        y = m.addVars(hidden, lb=0.0, name=f"y_k{k}")
        z = m.addVars(hidden, vtype=GRB.BINARY, name=f"z_k{k}")

        L = -w_bound * 5.0 - w_bound
        U =  w_bound * 5.0 + w_bound

        for j in range(hidden):
            m.addConstr(
                s[j] == W1[j, 0]*x_t + W1[j, 1]*x_bmin +
                        W1[j, 2]*x_bmax + W1[j, 3]*x_bbar +
                        W1[j, 4]*x_rt + b1[j],
                name=f"sdef_k{k}_j{j}"
            )
            m.addConstr(y[j] >= s[j],                name=f"relu_ge_k{k}_j{j}")
            m.addConstr(y[j] <= s[j] - L*(1 - z[j]), name=f"relu_on_k{k}_j{j}")
            m.addConstr(y[j] <= U * z[j],            name=f"relu_off_k{k}_j{j}")

        vhat_k = m.addVar(lb=-GRB.INFINITY, name=f"vhat_k{k}")
        m.addConstr(
            vhat_k == gp.quicksum(W2[j]*y[j] for j in range(hidden)) + b2,
            name=f"out_k{k}"
        )
        vhat_vars.append(vhat_k)

    obj_terms = []
    for k in range(K):
        served_k = gp.quicksum(x[v, k] for v in range(V))
        obj_terms.append((gamma ** k) * (served_k + lam * vhat_vars[k]))

    m.setObjective(gp.quicksum(obj_terms), GRB.MAXIMIZE)
    m.optimize()

    W1_np = np.array([[W1[j, i].X for i in range(5)] for j in range(hidden)],
                     dtype=float)
    b1_np = np.array([b1[j].X for j in range(hidden)], dtype=float)
    W2_np = np.array([W2[j].X for j in range(hidden)], dtype=float)
    b2_sc = float(b2.X)

    joint_inputs = []
    for k in range(K):
        Rvk = [R[v, k].X for v in range(V)]
        if Rvk:
            Bmin_val = min(Rvk)
            Bmax_val = max(Rvk)
            Bbar_val = sum(Rvk) / len(Rvk)
        else:
            Bmin_val = Bmax_val = Bbar_val = 0.0
        RT_val = RTres_vars[k].X
        t_k = base_state.t + k*dt
        t_phase = (t_k % T) / T
        x_vec = np.array([
            t_phase,
            min(1.0, Bmin_val / T),
            min(1.0, Bmax_val / T),
            min(1.0, Bbar_val / T),
            min(1.0, RT_val   / T)
        ], dtype=float)
        joint_inputs.append(x_vec)

    return W1_np, b1_np, W2_np, b2_sc, joint_inputs

