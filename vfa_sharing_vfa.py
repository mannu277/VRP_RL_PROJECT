def diagnose_vfa_one_epoch(relu_model,
                           grid_side=3,
                           area=5.0,
                           n_vehicles=3,
                           n_requests=3,
                           max_assignments=2000,
                           seed=999):
    """
    Diagnostic: for a small toy state, enumerate all feasible global plan
    combinations (one plan per vehicle, each request at most once), compute:
      - served (number of requests served)
      - Vhat (NN prediction on feature vector x)
    and print them.
    """

    print("\n===== VFA DIAGNOSTIC (one epoch) =====")

    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)

    # Small toy grid
    coords, tt = make_grid(grid_side, area)
    P_dummy = make_dummy_points(JOINT_GRID_SIDE, 2.0)

    # Construct a small fleet explicitly (3 vehicles: top-left, center, bottom-right)
    N = len(coords)
    center = N // 2
    vehicles = [
        Vehicle(0, 2, 0.0, 0),
        Vehicle(1, 2, 0.0, center),
        Vehicle(2, 2, 0.0, N - 1),
    ]
    vehicles = vehicles[:n_vehicles]

    state = State(T=10.0, t=0.0, tt=tt, coords=coords, vehicles=vehicles)

    # Generate a small pending set
    pending: List[Request] = []
    for _ in range(n_requests):
        r = generate_request(
            state,
            slack_low=BASE_SLACK_LOW * DEMAND_MULT,
            slack_high=BASE_SLACK_HIGH * DEMAND_MULT,
            service_time=1.0,
        )
        pending.append(r)

    print(f"Grid side: {grid_side}, #vehicles: {len(state.vehicles)}, "
          f"#pending requests: {len(pending)}")

    # Build per-vehicle plans exactly as in plan_epoch_with_relu_vfa
    Vset = list(range(len(state.vehicles)))
    vehicle_plans = []
    for v in Vset:
        plans_v = enumerate_vehicle_plans(state, pending, v, service_time=1.0)
        if not plans_v:
            plans_v = [{
                "served_idx": [],
                "route": [],
                "B": state.vehicles[v].time,
                "last_node": state.vehicles[v].node,
            }]
        vehicle_plans.append(plans_v)

    # Enumerate all global assignments: one plan per vehicle, each request at most once
    assignments = []

    def recurse(v_idx, current_choice, used_requests):
        if len(assignments) >= max_assignments:
            return
        if v_idx == len(vehicle_plans):
            assignments.append(list(current_choice))
            return
        for p_idx, plan in enumerate(vehicle_plans[v_idx]):
            s_idx = plan["served_idx"]
            # enforce "each request at most once"
            if any(k in used_requests for k in s_idx):
                continue
            current_choice.append(p_idx)
            new_used = used_requests.union(s_idx)
            recurse(v_idx + 1, current_choice, new_used)
            current_choice.pop()

    recurse(0, [], set())

    print(f"Enumerated {len(assignments)} feasible global assignments "
          f"(capped at {max_assignments}).")

    # Evaluate served and Vhat for each assignment
    rows = []
    for assign_id, choice in enumerate(assignments):
        # Build Action dict: for each vehicle v, pick its chosen plan route
        action: Action = {}
        total_served = 0
        for v, p_idx in enumerate(choice):
            plan = vehicle_plans[v][p_idx]
            if plan["served_idx"]:
                action[v] = list(plan["route"])
                total_served += len(plan["served_idx"])

        # Compute features for this state-action (post-decision features)
        z, Bmin_res, Bmax_res, Bbar_res, RT_res = aggregate_features_residual(
            state, action, P_dummy, service_time=1.0
        )
        # Same x-structure as in training / MILP VFA
        x = np.array([
            z[0],  # t_phase
            min(1.0, Bmin_res / state.T),
            min(1.0, Bmax_res / state.T),
            min(1.0, Bbar_res / state.T),
            min(1.0, RT_res   / state.T),
        ], dtype=float)

        vhat = relu_model.predict(x)
        rows.append((assign_id, total_served, vhat, x))

    # Sort by (served desc, vhat desc)
    rows.sort(key=lambda r: (-r[1], -r[2]))

    print("\nassignment_id | served | Vhat        | x")
    print("-----------------------------------------------")
    for assign_id, served, vhat, x in rows:
        print(f"{assign_id:12d} | {served:6d} | {vhat:10.4f} | "
              f"[{x[0]:.3f}, {x[1]:.3f}, {x[2]:.3f}, {x[3]:.3f}, {x[4]:.3f}]")

    print("===== END VFA DIAGNOSTIC =====\n")


