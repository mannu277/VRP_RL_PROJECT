def step_sim(state: State, pending: List[Request], weights, dq_v, b_k, debug=False):
    """
    Execute one epoch: solve MILP with modified parameters and update state.

    Debug mode (debug=True):
    When debugging is enabled, prints detailed information about the decision process:
      - Current time and pending request queue
      - For each request: pickup→drop, passengers, trip length, slack time, weight, base_value
      - Vehicle availability (location and time)
      - MILP assignment decisions (which vehicle serves which request)
      - Final reward calculation (base_value × weight for served requests)

    Use this to understand:
      1. Why certain requests are/aren't served (feasibility, weights, vehicle availability)
      2. Whether the policy is learning to assign high weights to valuable requests
      3. How the MILP solver is making assignment decisions

    Typical usage in run_episode_pg():
      debug_epochs=[0, 10, 20] if ep in [1, 5, 30] else None
      # This debugs epochs 0,10,20 of episodes 1,5,30 to see learning progress
    """
    action, served, milp_diag = solve_epoch_milp_modified(state, pending, weights, dq_v, b_k, TIME_LIMIT_MIP)

    # Debug output
    if debug:
        print(f"\n  Time t={state.t:.1f}")
        print(f"  Pending requests: {len(pending)}")
        for i, req in enumerate(pending):
            trip_len = state.tt[req.pickup, req.drop]
            slack = req.deadline - state.t - trip_len - 2*SERVICE_TIME
            print(f"    Req {i}: ({req.pickup}→{req.drop}) pax={req.pax} trip={trip_len:.2f} slack={slack:.2f} w={weights[i]:.3f} BASE_VALUE={req.base_value:.1f}")  # NEW: Show base value

        print(f"  Vehicle states:")
        for v in state.vehicles:
            print(f"    Veh {v.id}: at node {v.node}, available at t={v.time:.1f}")

        if action:
            print(f"  → Assigned {len(action)} vehicles:")
            for v_id, route in action.items():
                req_idx = served[list(action.keys()).index(v_id)]
                req = pending[req_idx]
                print(f"     Veh {v_id} serves Req {req_idx} (w={weights[req_idx]:.3f}, base_value={req.base_value:.1f}, reward={req.base_value * weights[req_idx]:.3f})")
        else:
            print(f"  → No assignment (all infeasible)")

    # Reward = sum of BASE VALUES of served requests (If base_value==1.0 the it's the normal MILP)
    r_train = sum(pending[k].base_value for k in served) if served else 0.0
    count_served = len(served)

    # Penalties (unchanged)
    if B_PENALTY > 0 and len(b_k) > 0:
        r_train -= B_PENALTY * float(np.mean(b_k))
    if DQ_PENALTY > 0 and len(dq_v) > 0:
        r_train -= DQ_PENALTY * float(np.mean(dq_v))

    # Apply ALL vehicle actions
    if action:
        for v_id, route in action.items():
            v = state.vehicles[v_id]
            t = v.time
            node = v.node
            for (i, j) in route:
                t += state.tt[i, j] + SERVICE_TIME
                node = j
            v.time = t
            v.node = node

    # Remove served requests from pending queue (unchanged)
    for idx in sorted(served, reverse=True):
        pending.pop(idx)

    state.t += DT
    return r_train, count_served, served, pending, milp_diag

