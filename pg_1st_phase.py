def train_single_seed(seed, outdir=OUTDIR, log_curve=True):
    print(f"Beginning training for seed {seed}...")
    random.seed(seed); np.random.seed(seed); torch.manual_seed(seed)
    print("Loading coords and tt...")
    coords,tt=make_grid(GRID_SIDE,AREA)
    policy=ModifierNet()
    critic=Critic()
    ent_coef = ENT0
    opt_p=torch.optim.Adam(policy.parameters(), lr=LR)
    opt_c=torch.optim.Adam(critic.parameters(), lr=LR)

    ep_returns=[]; std_scale=1.0
    snap_evals = []  # NEW: Track snap eval performance
    best_eval=-1e-9; best_state=None
    diag_rows=[]

    for ep in range(1, TRAIN_EPISODES+1):
        phase = phase_for_ep(ep)

        with torch.no_grad():
            policy.logstd_b.data  = torch.log(torch.clamp(torch.exp(policy.logstd_b)*std_scale, 1e-3, 1.0))
            policy.logstd_w.data  = torch.log(torch.clamp(torch.exp(policy.logstd_w)*std_scale, 1e-3, 1.0))
            policy.logstd_dq.data = torch.log(torch.clamp(torch.exp(policy.logstd_dq)*std_scale, 1e-3, 1.0))

        v_target, G_t, adv, gstack, total_served, diag, logp_w_steps, ent_w_steps, served_steps = run_episode_pg(
            coords, tt, policy, critic, ent_coef, phase=phase,
            debug_epochs=None
            #debug_epochs=[0, 10, 20] if ep in [1, 5, 30] else None  # Debug first 3 epochs of episodes 1, 5, 30
        )
        ep_returns.append(total_served)

        diag_rows.append({
            "episode": ep,
            "phase": phase,
            "avg_b": diag["avg_b"],
            "gate_frac": diag["gate_frac"],
            "avg_dq": diag["avg_dq"],
            "neg_w_frac": diag["neg_w_frac"],
            "feas_density": diag["feas_density"],
            "ent": diag["ent"],
            "mip_status": diag["mip_status"],
            "mip_runtime": diag["mip_runtime"],
            "mip_gap": diag["mip_gap"],
            "served": total_served,
            "avg_queue_size": diag["avg_queue_size"],
            "max_queue_size": diag["max_queue_size"],
            "avg_w": diag["avg_w"],
            "std_w": diag["std_w"],
            "min_w": diag["min_w"],
            "max_w": diag["max_w"],
        })

        # Critic update
        pred=critic(gstack)
        v_loss=F.mse_loss(pred, v_target)
        opt_c.zero_grad(); v_loss.backward(); nn.utils.clip_grad_norm_(critic.parameters(), GRAD_CLIP); opt_c.step()

        # === Policy update: served-only credit assignment ===
        at = adv.detach()  # REINFORCE: treat advantage as constant
        pg_terms  = []
        ent_terms = []
        for t in range(len(logp_w_steps)):
            lp_vec  = logp_w_steps[t]    # tensor [K_t]
            ent_vec = ent_w_steps[t]     # tensor [K_t]
            served  = served_steps[t]    # list[int]

            if len(served) > 0:
                idx = torch.tensor(served, dtype=torch.long, device=lp_vec.device)
                lp_served  = lp_vec.index_select(0, idx).sum()
                ent_served = ent_vec.index_select(0, idx).sum()
            else:
                zero = lp_vec.sum() * 0.0
                lp_served  = zero
                ent_served = zero

            pg_terms.append(- at[t] * lp_served)
            ent_terms.append(- ent_coef * ent_served)

        pg_loss = torch.stack(pg_terms).sum() + torch.stack(ent_terms).sum()

        opt_p.zero_grad()
        pg_loss.backward()
        nn.utils.clip_grad_norm_(policy.parameters(), GRAD_CLIP)
        opt_p.step()

        ent_coef *= ENT_DECAY

        if (ep % 100) == 0 and log_curve:
            print(f"[train s{seed}] ep={ep:04d} served={total_served:.1f} queue={diag['avg_queue_size']:.1f} w={diag['min_w']:.2f}-{diag['max_w']:.2f} (\u03bc={diag['avg_w']:.2f} \u03c3={diag['std_w']:.2f}) phase={phase}")

        if ep in (CURR_W_ONLY_EP, CURR_WB_ONLY_EP):
            snap = np.mean([run_episode_pg_eval(coords, tt, policy, phase=phase) for _ in range(20)])
            tag = "w-only" if ep==CURR_W_ONLY_EP else "w+b"
            print(f"[seed {seed}] end {tag} eval: {snap:.2f}")

        if (ep % EVAL_EVERY) == 0:
            snap_pg = np.mean([run_episode_pg_eval(coords, tt, policy, phase=phase)
                              for _ in range(SNAP_EVAL_EPISODES)])
            snap_evals.append((ep, snap_pg))  # NEW: Save (episode, performance)

            if snap_pg > best_eval:
                best_eval = snap_pg
                best_state = {
                    "policy": {k: v.cpu().clone() for k, v in policy.state_dict().items()},
                    "critic": {k: v.cpu().clone() for k, v in critic.state_dict().items()},
                    "ep": ep,
                    "phase": phase,
                }
            print(f"[train s{seed}] ep={ep:04d} snap_eval={snap_pg:.4f} best={best_eval:.4f}")

    if best_state is not None:
        policy.load_state_dict(best_state["policy"])
        critic.load_state_dict(best_state["critic"])
        print(f"[seed {seed}] restored best snapshot from ep={best_state['ep']} (phase={best_state['phase']}, eval\u2248{best_eval:.2f})")
        best_phase = best_state["phase"]
    else:
        best_phase = phase_for_ep(TRAIN_EPISODES)

    print("Running evaluation for PG-modifier...")
    pg_eval   = np.mean([run_episode_pg_eval(coords,tt,policy, phase=best_phase) for _ in range(EVAL_EPISODES)])
    print("Running evaluation for NoMod baseline...")
    nomod_eval= np.mean([run_episode_nomod(coords,tt) for _ in range(EVAL_EPISODES)])
    print("Running evaluation for Heuristic baseline...")
    heur_eval = np.mean([run_episode_heur(coords,tt) for _ in range(EVAL_EPISODES)])

    Path(outdir).mkdir(exist_ok=True)
    with open(os.path.join(outdir, f"curve_seed{seed}.csv"), "w", newline="") as f:
        w = csv.writer(f); w.writerow(["episode","served"])
        for i,v in enumerate(ep_returns,1): w.writerow([i,v])
    with open(os.path.join(outdir, f"diag_seed{seed}.csv"), "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=[
            "episode","phase","avg_b","gate_frac","avg_dq","neg_w_frac","feas_density","ent",
            "mip_status","mip_runtime","mip_gap","served","avg_queue_size","max_queue_size",
            "avg_w","std_w","min_w","max_w"
        ])
        w.writeheader(); w.writerows(diag_rows)

    return ep_returns, snap_evals, pg_eval, nomod_eval, heur_eval  # NEW: return snap_evals

