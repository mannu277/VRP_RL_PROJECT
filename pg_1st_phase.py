class ModifierNet(nn.Module):
    """
    π_MOD: outputs per-request (b_k deadline tighten, w_k reward weight) and per-vehicle (ΔQ_v capacity tighten).
    Range constraints enforce “only tighten / decrease” and permit negative rewards (W_MIN < 0).
    """
    def __init__(self,d_global=6,d_req=5,d_veh=2,h=64):
        super().__init__()
        print("Loading policy...")
        # Per-request processing (NO shared global bottleneck)
        self.req_encoder = nn.Sequential(
            nn.Linear(d_req + d_global, h),
            nn.ReLU(),
            nn.Linear(h, h),
            nn.ReLU()
        )
        self.req_head = nn.Sequential(
            nn.Linear(h, h),
            nn.ReLU(),
            nn.Linear(h, 2)  # [b_raw, w_raw]
        )

        # Per-vehicle processing
        self.veh_encoder = nn.Sequential(
            nn.Linear(d_veh + d_global, h),
            nn.ReLU(),
            nn.Linear(h, h),
            nn.ReLU()
        )
        self.veh_head = nn.Sequential(
            nn.Linear(h, h),
            nn.ReLU(),
            nn.Linear(h, 1)  # [dq_raw]
        )

        self.logstd_b=nn.Parameter(torch.tensor(math.log(STD_B0)))
        self.logstd_w=nn.Parameter(torch.tensor(math.log(STD_W0)))
        self.logstd_dq=nn.Parameter(torch.tensor(math.log(STD_DQ0)))

        # Better initialization
        with torch.no_grad():
            for m in self.modules():
                if isinstance(m, nn.Linear):
                    nn.init.orthogonal_(m.weight, gain=0.5)
                    if m.bias is not None:
                        nn.init.zeros_(m.bias)

    @staticmethod
    def _sq_b(y):  return CLIP_MAX_B*torch.sigmoid(y)
    @staticmethod
    def _sq_w(y):  return W_MIN+(W_MAX-W_MIN)*torch.sigmoid(y)
    @staticmethod
    def _sq_dq(y): return CLIP_MAX_DQ*torch.sigmoid(y)

    def forward_means(self,gvec,rmat,vmat):
        # Expand global to match batch
        g_expanded_req = gvec.unsqueeze(0).expand(rmat.shape[0], -1) if rmat.shape[0] > 0 else gvec.unsqueeze(0)
        g_expanded_veh = gvec.unsqueeze(0).expand(vmat.shape[0], -1) if vmat.shape[0] > 0 else gvec.unsqueeze(0)

        # Process each request independently
        if rmat.shape[0] > 0:
            req_input = torch.cat([rmat, g_expanded_req], dim=1)
            req_encoded = self.req_encoder(req_input)
            mu_req = self.req_head(req_encoded)
        else:
            mu_req = torch.zeros(1, 2, device=gvec.device)

        # Process each vehicle independently
        if vmat.shape[0] > 0:
            veh_input = torch.cat([vmat, g_expanded_veh], dim=1)
            veh_encoded = self.veh_encoder(veh_input)
            mu_veh = self.veh_head(veh_encoded).squeeze(-1)
        else:
            mu_veh = torch.zeros(1, device=gvec.device)

        return mu_req, mu_veh

    def sample(self, gvec, rmat, vmat, allow_b=True, allow_dq=False, slack_gate=SLACK_GATE):
      mu_req, mu_veh = self.forward_means(gvec, rmat, vmat)
      dist_b  = D.Normal(mu_req[:,0], self.logstd_b.exp())
      dist_w  = D.Normal(mu_req[:,1], self.logstd_w.exp())
      dist_dq = D.Normal(mu_veh,     self.logstd_dq.exp())

      b_raw  = dist_b.rsample()
      w_raw  = dist_w.rsample()
      dq_raw = dist_dq.rsample()

      b_k  = self._sq_b(b_raw)
      w_k  = self._sq_w(w_raw)
      dq_v = self._sq_dq(dq_raw)

      # slack gating for b
      if rmat.numel() > 0:
          mask = (rmat[:,2] > slack_gate).float()
      else:
          mask = torch.zeros_like(b_k)

      if allow_b:  b_k = b_k * mask
      else:        b_k = torch.zeros_like(b_k)
      if not allow_dq: dq_v = torch.zeros_like(dq_v)

      # === build loss pieces only for enabled heads ===
      # Why separate losses? During curriculum learning, we enable heads gradually:
      #   Phase 1 (eps 1-1000):    only w (weights) - learn basic prioritization
      #   Phase 2 (eps 1001-2000): w + b (deadlines) - learn to tighten time windows
      #   Phase 3 (eps 2001+):     w + b + ΔQ (capacity) - learn capacity management
      #
      # We ONLY compute gradients for active heads. Otherwise:
      #   - Inactive heads get random gradients (they're not used by MILP)
      #   - This adds noise and slows learning
      #   - Wastes compute on unused parameters
      #
      # So we build logp and entropy terms conditionally:
      logp_terms = [dist_w.log_prob(w_raw).sum()]  # w always active
      ent_terms  = [dist_w.entropy().sum()]

      if allow_b:  # Add b terms only if deadline tightening is enabled
          logp_terms.append(dist_b.log_prob(b_raw).sum())
          ent_terms.append(dist_b.entropy().sum())

      if allow_dq:  # Add ΔQ terms only if capacity modification is enabled
          logp_terms.append(dist_dq.log_prob(dq_raw).sum())
          ent_terms.append(dist_dq.entropy().sum())

      logp = torch.stack(logp_terms).sum()
      ent  = torch.stack(ent_terms).sum()

      # Result: Gradients only flow through heads that are actually being used!

      # return also per-request log-prob for w to enable per-served credit
      logp_w_vec = dist_w.log_prob(w_raw)    # shape [K], keep grads
      ent_w_vec  = dist_w.entropy()          # shape [K]

      return (
          b_k.detach().numpy().tolist(),
          dq_v.detach().numpy().tolist(),
          w_k.detach().numpy().tolist(),
          logp, ent, float(mask.mean()),
          logp_w_vec, ent_w_vec
      )

    def deterministic(self,gvec,rmat,vmat, allow_b=True, allow_dq=False, slack_gate=SLACK_GATE):
        mu_req,mu_veh=self.forward_means(gvec,rmat,vmat)
        b=self._sq_b(mu_req[:,0]); dq=self._sq_dq(mu_veh); w=self._sq_w(mu_req[:,1])

        if rmat.numel() > 0:
            slack=rmat[:,2]; mask=(slack > slack_gate).float()
        else:
            mask = torch.zeros_like(b)

        if allow_b:  b = b * mask
        else:        b = torch.zeros_like(b)
        if not allow_dq: dq = torch.zeros_like(dq)

        return b.detach().numpy().tolist(), dq.detach().numpy().tolist(), w.detach().numpy().tolist()

