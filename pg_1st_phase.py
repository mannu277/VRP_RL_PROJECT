# ========= FEATURES ======
def req_features(state:State, pending:List[Request]):
    feats=[]
    for r in pending:
        trip=state.tt[r.pickup,r.drop]
        slack=(r.deadline-state.t)-(trip+2*SERVICE_TIME)
        feats.append([trip, r.pax, max(0.0,slack), float(r.pickup), float(r.drop)])
    if not feats: feats=[[0,0,0,0,0]]
    M=max(1,len(state.coords)-1)
    f=np.array(feats,dtype=np.float32)
    f[:,0]=f[:,0]/(AREA*2+1e-6)
    f[:,1]=f[:,1]/max(1,VEH_CAP)
    f[:,2]=f[:,2]/5.0
    f[:,3]=f[:,3]/M; f[:,4]=f[:,4]/M
    return f

def veh_features(state:State):
    feats=[]; N=len(state.coords); M=max(1,N-1)
    for v in state.vehicles:
        feats.append([float(v.node)/M, max(0.0, v.time-state.t)])
    f=np.array(feats,dtype=np.float32)
    if f.size==0: f=np.zeros((1,2),dtype=np.float32)
    f[:,1]=np.clip(f[:,1],0.0,5.0)/5.0
    return f

def global_features(state:State, pending:List[Request]):
    rfe=req_features(state,pending)
    slack_stats=[rfe[:,2].min(),rfe[:,2].max(),rfe[:,2].mean()] if len(rfe)>0 else [0,0,0]
    t_phase=(state.t%max(1e-6,state.T))/max(1e-6,state.T)
    feas_count = 0
    for v in state.vehicles:
        for r in pending:
            arrp = v.time + state.tt[v.node, r.pickup] + SERVICE_TIME
            arrd = arrp + state.tt[r.pickup, r.drop] + SERVICE_TIME
            feas_count += int(arrd <= r.deadline and r.pax <= v.capacity)
    denom = max(1, len(state.vehicles) * len(pending))
    feas_density = feas_count / denom
    return np.array([t_phase, len(pending)/8.0, slack_stats[0], slack_stats[1], slack_stats[2], feas_density], dtype=np.float32)

