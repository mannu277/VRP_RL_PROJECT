# ========= MILP ==========
def solve_epoch_milp_modified(state:State, pending:List[Request],
                              weights:List[float], cap_tighten:List[float], deadline_tighten:List[float],
                              timelimit=TIME_LIMIT_MIP):
    """
    Solve a *single-assignment* epoch MILP on the MODIFIED instance:
      - Per-request reward (weight) can vary (prioritization)
      - Per-vehicle capacity is only decreased (cap - ΔQ >= 0).
      - Per-request deadline can only be tightened (deadline - b, with b ≥ 0 and ≤ slack).
    Feasibility domain is a subset of the original (Ω(mod) ⊆ Ω(orig)).
    Returns: (action, served_idx, milp_diag)
    """
    V=list(range(len(state.vehicles))); K=list(range(len(pending)))
    v_node=[state.vehicles[v].node for v in V]; v_time=[state.vehicles[v].time for v in V]
    base_cap=[state.vehicles[v].capacity for v in V]
    eff_cap=[max(0.0, base_cap[v]-float(cap_tighten[v])) for v in V]

    eff_dead=[]
    for k in K:
        r=pending[k]
        min_time=state.tt[r.pickup,r.drop]+2*SERVICE_TIME
        slack=max(0.0,(r.deadline-state.t)-min_time)
        b=min(max(0.0,float(deadline_tighten[k])), slack)
        eff_dead.append(max(state.t, r.deadline-b))

    feas={(v,k):1 for v in V for k in K}
    for v in V:
      for k in K:
          r=pending[k]

          # Check if vehicle is available at current time
          if v_time[v] > state.t:
              feas[(v,k)] = 0
              continue

          if r.pax>eff_cap[v]: feas[(v,k)]=0; continue
          arr_p=v_time[v]+state.tt[v_node[v],r.pickup]+SERVICE_TIME
          arr_d=arr_p+state.tt[r.pickup,r.drop]+SERVICE_TIME
          if arr_d>eff_dead[k]: feas[(v,k)]=0; continue

    m = gp.Model("epoch_mod", env=GLOBAL_ENV)
    m.Params.OutputFlag = 0
    m.Params.TimeLimit = timelimit
    x = m.addVars(V, K, vtype=GRB.BINARY, name="x")

    for k in K: m.addConstr(gp.quicksum(x[v,k] for v in V) <= 1)
    for v in V:
        m.addConstr(gp.quicksum(x[v,k] for k in K) <= 1)
        m.addConstr(gp.quicksum(pending[k].pax*x[v,k] for k in K if feas[(v,k)]==1) <= eff_cap[v])
    for v in V:
        for k in K:
            if feas[(v,k)]==0: m.addConstr(x[v,k]==0)

    m.setObjective(gp.quicksum(float(weights[k])*x[v,k] for v in V for k in K if feas[(v,k)]==1), GRB.MAXIMIZE)
    m.optimize()

    # collect solver diagnostics
    status  = int(m.Status)
    runtime = float(getattr(m, "Runtime", float("nan")))
    mipgap  = float(getattr(m, "MIPGap", float("nan")))
    milp_diag = dict(status=status, runtime=runtime, mipgap=mipgap)

    # Extract ALL assignments (all vehicles)
    action:Action={}
    served_idx=[]

    for v in V:
        for k in K:
            if feas[(v,k)]==1 and x[v,k].X > 0.5:
                req=pending[k]
                action[v]=[(state.vehicles[v].node, req.pickup),(req.pickup, req.drop)]
                served_idx.append(k)
                # NO BREAK - keep checking (though MILP ensures at most 1 per vehicle)
                # There was a problem here: the MILP was assigning always a single
                # vehicle, even if multiple were available.

    return action, served_idx, milp_diag

