def run_episode_pg(coords,tt,policy:ModifierNet,critic:Critic=None,ent_coef=0.0, phase="w", debug_epochs=None):
    """
    debug_epochs: list of epoch indices to print detailed debug info (e.g., [0, 10, 20])
    """
    gates = phase_to_gates(phase)
    st=make_initial_state(coords,tt); pending=[]; logps=[]; ents=[]; train_rewards=[]; true_counts=[]; gbuf=[]
    diag_avg_b=[]; diag_gate_frac=[]; diag_avg_dq=[]
    diag_neg_w=[]; diag_feas=[]; diag_ent=[]
    diag_mip_status=[]; diag_mip_runtime=[]; diag_mip_gap=[]
    diag_queue_size=[]; diag_avg_w=[]; diag_std_w=[]; diag_min_w=[]; diag_max_w=[]

    # buffers before the loop
    logp_w_steps = []
    ent_w_steps  = []
    served_steps = []

    for epoch_idx in range(EP_STEPS):
        debug_this_epoch = debug_epochs is not None and epoch_idx in debug_epochs

        if debug_this_epoch:
            print(f"\n{'='*60}")
            print(f"EPOCH {epoch_idx}/{EP_STEPS}")
            print(f"{'='*60}")

        for _ in range(REQUESTS_PER_EPOCH):
            req=generate_request(st, slack_low=0.0, slack_high=0.1) # Increased slack
            pending.append(req)

        pending=[r for r in pending if r.deadline>=st.t]
        diag_queue_size.append(len(pending))
        (out, g) = plan_epoch_with_pg_modifiers(st, pending, policy, **gates)
        (b, dq, w, logp_scalar, ent_scalar, gate_frac, logp_w_vec, ent_w_vec) = out

        # keep scalar diagnostics if you still want them:
        logps.append(logp_scalar)
        ents.append(ent_scalar)
        diag_ent.append(float(ent_scalar))   # <-- add this line

        r_train, count, served, pending, milp_d = step_sim(st, pending, w, dq, b, debug=debug_this_epoch)

        # store the per-request vectors & served indices for this step
        logp_w_steps.append(logp_w_vec)   # tensor length K_t
        ent_w_steps.append(ent_w_vec)     # tensor length K_t
        served_steps.append(served)       # python list of ints

        if debug_this_epoch:
            print(f"\nModifier outputs:")
            print(f"  Weight stats: min={min(w) if w else 0:.3f} max={max(w) if w else 0:.3f} mean={np.mean(w) if w else 0:.3f} std={np.std(w) if w else 0:.3f}")


        train_rewards.append(r_train)
        true_counts.append(count)
        gbuf.append(g)

        diag_avg_b.append(float(np.mean(b)) if len(b)>0 else 0.0)
        diag_gate_frac.append(gate_frac)
        diag_avg_dq.append(float(np.mean(dq)) if len(dq)>0 else 0.0)
        diag_avg_w.append(float(np.mean(w)) if len(w)>0 else 1.0)
        diag_std_w.append(float(np.std(w)) if len(w)>1 else 0.0)
        diag_min_w.append(float(np.min(w)) if len(w)>0 else 1.0)
        diag_max_w.append(float(np.max(w)) if len(w)>0 else 1.0)

        neg_w_frac = float(np.mean(np.array(w) < 1.0)) if len(w)>0 else 0.0
        diag_neg_w.append(neg_w_frac)
        diag_feas.append(float(g[5].item()))
        diag_mip_status.append(milp_d["status"])
        diag_mip_runtime.append(milp_d["runtime"])
        diag_mip_gap.append(milp_d["mipgap"])

    # Returns from training rewards
    G=[]; acc=0.0
    for r in reversed(train_rewards):
        acc=r+GAMMA*acc; G.append(acc)
    G=list(reversed(G))
    G_t=torch.tensor(G,dtype=torch.float32)
    gstack=torch.stack(gbuf)

    if critic is None:
        adv=G_t
        v_for_loss = G_t.detach()
    else:
        with torch.no_grad():
            bval=critic(gstack)
        adv = G_t - bval
        v_for_loss = G_t.detach()
        adv = (adv - adv.mean())/(adv.std()+1e-6)

    diag = dict(
        avg_b=float(np.mean(diag_avg_b)),
        gate_frac=float(np.mean(diag_gate_frac)),
        avg_dq=float(np.mean(diag_avg_dq)),
        neg_w_frac=float(np.mean(diag_neg_w)),
        feas_density=float(np.mean(diag_feas)),
        ent=float(np.mean(diag_ent)),
        mip_status=int(round(statistics.mean(diag_mip_status))) if len(diag_mip_status)>0 else -1,
        mip_runtime=float(np.mean(diag_mip_runtime)),
        mip_gap=float(np.nanmean(diag_mip_gap)) if np.any(~np.isnan(diag_mip_gap)) else float("nan"),
        avg_queue_size=float(np.mean(diag_queue_size)),
        max_queue_size=float(np.max(diag_queue_size)),
        avg_w=float(np.mean(diag_avg_w)),
        std_w=float(np.mean(diag_std_w)),
        min_w=float(np.mean(diag_min_w)),
        max_w=float(np.mean(diag_max_w)),
    )

    total_served = sum(true_counts)
  # Return ONLY what we need; drop old logps/ents
    return v_for_loss, G_t, adv, gstack, total_served, diag, logp_w_steps, ent_w_steps, served_steps


def run_episode_pg_eval(coords,tt,policy:ModifierNet, phase="w"):
    gates = phase_to_gates(phase)
    st=make_initial_state(coords,tt); pending=[]; tot=0
    for _ in range(EP_STEPS):
        for _ in range(REQUESTS_PER_EPOCH):
            req=generate_request(st)
            pending.append(req)

        pending=[r for r in pending if r.deadline>=st.t]
        g=torch.tensor(global_features(st,pending),dtype=torch.float32)
        rfe=torch.tensor(req_features(st,pending),dtype=torch.float32)
        vfe=torch.tensor(veh_features(st),dtype=torch.float32)
        b,dq,w = policy.deterministic(g,rfe,vfe, **gates)

        action,served,_ = solve_epoch_milp_modified(st,pending,w,dq,b,TIME_LIMIT_MIP)
        tot+=len(served)

        # Apply ALL vehicle actions
        if action:
            for v_id, route in action.items():
                v = st.vehicles[v_id]
                t = v.time
                node = v.node
                for (i, j) in route:
                    t += st.tt[i, j] + SERVICE_TIME
                    node = j
                v.time = t
                v.node = node

        for idx in sorted(served,reverse=True): pending.pop(idx)
        st.t+=DT
    return tot

