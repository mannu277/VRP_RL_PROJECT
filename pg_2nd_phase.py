def make_grid(n_side: int, area: float):
    coords = []
    step = area / (n_side - 1) if n_side > 1 else 0.0
    for r in range(n_side):
        for c in range(n_side):
            coords.append((r * step, c * step))

    N = n_side * n_side
    tt = np.zeros((N, N), dtype=float)
    for i, (xi, yi) in enumerate(coords):
        for j, (xj, yj) in enumerate(coords):
            tt[i, j] = 0.0 if i == j else math.hypot(xi - xj, yi - yj)
    return coords, tt

def generate_request(state: State, pax_min=1, pax_max=2, slack_low=0.0, slack_high=0.3):
    N = len(state.coords)
    p = random.randrange(N)
    d = random.randrange(N)
    while d == p:
        d = random.randrange(N)

    pax = random.randint(pax_min, pax_max)
    travel = state.tt[p, d]
    deadline = state.t + travel + 2 * SERVICE_TIME + random.uniform(slack_low, slack_high)

    # You can test "learning to prioritize" by making base_value heterogeneous:
    base_value = 1.0
    # base_value = float(pax)
    # base_value = 3.0 if random.random() < 0.1 else 1.0

    return Request(pickup=p, drop=d, pax=pax, deadline=deadline, base_value=base_value)


