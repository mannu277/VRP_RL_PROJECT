def req_features(state: State, pending: List[Request]):
    feats = []
    for r in pending:
        trip = state.tt[r.pickup, r.drop]
        slack = (r.deadline - state.t) - (trip + 2 * SERVICE_TIME)
        feats.append([trip, r.pax, max(0.0, slack), float(r.pickup), float(r.drop)])
    if not feats:
        feats = [[0, 0, 0, 0, 0]]

    M = max(1, len(state.coords) - 1)
    f = np.array(feats, dtype=np.float32)

    # normalize
    f[:, 0] = f[:, 0] / (AREA * 2 + 1e-6)
    f[:, 1] = f[:, 1] / max(1, VEH_CAP)
    f[:, 2] = f[:, 2] / 5.0
    f[:, 3] = f[:, 3] / M
    f[:, 4] = f[:, 4] / M
    return f

def global_features(state: State, pending: List[Request]):
    rfe = req_features(state, pending)
    slack_stats = [rfe[:, 2].min(), rfe[:, 2].max(), rfe[:, 2].mean()] if len(rfe) > 0 else [0, 0, 0]
    t_phase = (state.t % max(1e-6, state.T)) / max(1e-6, state.T)

    feas_count = 0
    for v in state.vehicles:
        for r in pending:
            arrp = v.time + state.tt[v.node, r.pickup] + SERVICE_TIME
            arrd = arrp + state.tt[r.pickup, r.drop] + SERVICE_TIME
            feas_count += int(arrd <= r.deadline and r.pax <= v.capacity and v.time <= state.t)
    denom = max(1, len(state.vehicles) * max(1, len(pending)))
    feas_density = feas_count / denom

    base = np.array(
        [t_phase, len(pending) / 8.0, slack_stats[0], slack_stats[1], slack_stats[2], feas_density],
        dtype=np.float32
    )

    # extra aggregate stats (helps policy)
    if len(pending) > 0:
        trips = np.array([state.tt[r.pickup, r.drop] for r in pending], dtype=np.float32)
        pax = np.array([r.pax for r in pending], dtype=np.float32)
        slack_raw = np.array([(r.deadline - state.t) - (state.tt[r.pickup, r.drop] + 2 * SERVICE_TIME) for r in pending], dtype=np.float32)
        extras = np.array([
            float(np.mean(trips) / (AREA * 2 + 1e-6)),
            float(np.mean(pax) / max(1, VEH_CAP)),
            float(np.mean(np.clip(slack_raw, 0.0, 5.0)) / 5.0),
        ], dtype=np.float32)
    else:
        extras = np.zeros(3, dtype=np.float32)

    return np.concatenate([base, extras], axis=0)  # dim=9


# ============================================================
# MILP (single epoch)
# ============================================================
def solve_epoch_milp_modified(
    state: State,
    pending: List[Request],
    weights: List[float],
    cap_tighten: List[float],
    deadline_tighten: List[float],
    timelimit=TIME_LIMIT_MIP
):
    V = list(range(len(state.vehicles)))
    K = list(range(len(pending)))

    v_node = [state.vehicles[v].node for v in V]
    v_time = [state.vehicles[v].time for v in V]
    base_cap = [state.vehicles[v].capacity for v in V]
    eff_cap = [max(0.0, base_cap[v] - float(cap_tighten[v])) for v in V]

    eff_dead = []
    for k in K:
        r = pending[k]
        min_time = state.tt[r.pickup, r.drop] + 2 * SERVICE_TIME
        slack = max(0.0, (r.deadline - state.t) - min_time)
        b = min(max(0.0, float(deadline_tighten[k])), slack)
        eff_dead.append(max(state.t, r.deadline - b))

    feas = {(v, k): 1 for v in V for k in K}
    for v in V:
        for k in K:
            r = pending[k]
            if v_time[v] > state.t:
                feas[(v, k)] = 0
                continue
            if r.pax > eff_cap[v]:
                feas[(v, k)] = 0
                continue
            arr_p = v_time[v] + state.tt[v_node[v], r.pickup] + SERVICE_TIME
            arr_d = arr_p + state.tt[r.pickup, r.drop] + SERVICE_TIME
            if arr_d > eff_dead[k]:
                feas[(v, k)] = 0
                continue

    m = gp.Model("epoch_mod", env=GLOBAL_ENV)
    m.Params.OutputFlag = 0
    m.Params.TimeLimit = timelimit
    x = m.addVars(V, K, vtype=GRB.BINARY, name="x")

    for k in K:
        m.addConstr(gp.quicksum(x[v, k] for v in V) <= 1)
    for v in V:
        m.addConstr(gp.quicksum(x[v, k] for k in K) <= 1)

    for v in V:
        for k in K:
            if feas[(v, k)] == 0:
                m.addConstr(x[v, k] == 0)

    m.setObjective(
        gp.quicksum(float(weights[k]) * x[v, k] for v in V for k in K if feas[(v, k)] == 1),
        GRB.MAXIMIZE
    )
    m.optimize()

    status = int(m.Status)
    runtime = float(getattr(m, "Runtime", float("nan")))
    mipgap = float(getattr(m, "MIPGap", float("nan")))
    diag = dict(status=status, runtime=runtime, mipgap=mipgap)

    action: Action = {}
    served_idx = []
    for v in V:
        for k in K:
            if feas[(v, k)] == 1 and x[v, k].X > 0.5:
                req = pending[k]
                action[v] = [(state.vehicles[v].node, req.pickup), (req.pickup, req.drop)]
                served_idx.append(k)

    return action, served_idx, diag


