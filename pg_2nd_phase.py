# Candidate modes (small discrete action space)
# ============================================================
def build_candidate_modes() -> List[Dict[str, Any]]:
    modes = []

    def mode_nomod(state, pending):
        K = len(pending)
        V = len(state.vehicles)
        return [1.0]*K, [0.0]*K, [0.0]*V

    def mode_urgency(state, pending):
        V = len(state.vehicles)
        w = []
        for r in pending:
            trip = state.tt[r.pickup, r.drop]
            slack = (r.deadline - state.t) - (trip + 2*SERVICE_TIME)
            w.append(float(1.0 + 3.0*np.exp(-max(0.0, slack))))
        return w, [0.0]*len(pending), [0.0]*V

    def mode_shorttrip(state, pending):
        V = len(state.vehicles)
        w = []
        for r in pending:
            trip = state.tt[r.pickup, r.drop]
            w.append(float(1.0 + 3.0*(1.0/(1.0+trip))))
        return w, [0.0]*len(pending), [0.0]*V

    def mode_highpax(state, pending):
        V = len(state.vehicles)
        w = [float(1.0 + 2.0*r.pax) for r in pending]
        return w, [0.0]*len(pending), [0.0]*V

    def mode_deadline_buffer(state, pending):
        V = len(state.vehicles)
        w = [1.0]*len(pending)
        b = []
        for r in pending:
            trip = state.tt[r.pickup, r.drop]
            slack = (r.deadline - state.t) - (trip + 2*SERVICE_TIME)
            b.append(float(0.15 if slack > 0.2 else 0.0))
        return w, b, [0.0]*V

    def mode_random(state, pending):
        V = len(state.vehicles)
        w = [float(0.5 + 4.5*random.random()) for _ in pending]
        return w, [0.0]*len(pending), [0.0]*V

    modes.append({"name":"NoMod", "fn": mode_nomod})
    modes.append({"name":"Urgency", "fn": mode_urgency})
    modes.append({"name":"ShortTrip", "fn": mode_shorttrip})
    modes.append({"name":"HighPax", "fn": mode_highpax})
    modes.append({"name":"DeadlineBuf", "fn": mode_deadline_buffer})
    modes.append({"name":"Random", "fn": mode_random})
    return modes

CANDIDATE_MODES = build_candidate_modes()
N_MODES = len(CANDIDATE_MODES)

def generate_candidates(state: State, pending: List[Request], timelimit=TIME_LIMIT_MIP):
    cands = []
    for i, m in enumerate(CANDIDATE_MODES):
        w, b, dq = m["fn"](state, pending)
        action, served, diag = solve_epoch_milp_modified(state, pending, w, dq, b, timelimit=timelimit)
        r = sum(pending[k].base_value for k in served) if served else 0.0
        cands.append({
            "mode_id": i,
            "name": m["name"],
            "action": action,
            "served": served,
            "reward": float(r),
            "diag": diag
        })
    return cands


# ============================================================
# Policy: choose among N_MODES (discrete actions)
# ============================================================
class CandidatePolicyNet(nn.Module):
    def __init__(self, d_in=9, h=64, n_actions=N_MODES):
        super().__init__()
        print("Loading candidate-policy (Option A)...")
        self.net = nn.Sequential(
            nn.Linear(d_in, h),
            nn.ReLU(),
            nn.Linear(h, h),
            nn.ReLU(),
            nn.Linear(h, n_actions),
        )
        with torch.no_grad():
            for m in self.modules():
                if isinstance(m, nn.Linear):
                    nn.init.orthogonal_(m.weight, gain=0.7)
                    if m.bias is not None:
                        nn.init.zeros_(m.bias)

    def logits(self, g):
        return self.net(g)

    def sample(self, g):
        dist = D.Categorical(logits=self.logits(g))
        a = dist.sample()
        return int(a.item()), dist.log_prob(a), dist.entropy()

    def deterministic(self, g):
        return int(torch.argmax(self.logits(g)).item())

class Critic(nn.Module):
    def __init__(self, d_in=9, h=64):
        super().__init__()
        print("Loading critic...")
        self.net = nn.Sequential(nn.Linear(d_in,h), nn.ReLU(), nn.Linear(h,1))
    def forward(self, g):
        return self.net(g).squeeze(-1)


# ============================================================
# Rollout (train)
# ============================================================
def run_episode_optionA(coords, tt, policy: CandidatePolicyNet, critic: Critic, ent_coef=0.0, debug=False):
    st = make_initial_state(coords, tt)
    pending: List[Request] = []

    logps, ents, rewards, gbuf = [], [], [], []
    served_counts = []
    chosen_modes = []

    for step in range(EP_STEPS):
        for _ in range(REQUESTS_PER_EPOCH):
            pending.append(generate_request(st))
        pending = [r for r in pending if r.deadline >= st.t]

        g = torch.tensor(global_features(st, pending), dtype=torch.float32)

        # candidates via MILP search
        cands = generate_candidates(st, pending)

        # policy chooses a candidate
        a, logp, ent = policy.sample(g)
        chosen = cands[a]

        if debug:
            print(f"[step {step}] pending={len(pending)} choose={chosen['name']} served={len(chosen['served'])} r={chosen['reward']:.1f}")

        # execute chosen action
        apply_action_and_advance(st, pending, chosen["action"], chosen["served"])

        logps.append(logp)
        ents.append(ent)
        rewards.append(torch.tensor(chosen["reward"], dtype=torch.float32))
        gbuf.append(g)
        served_counts.append(len(chosen["served"]))
        chosen_modes.append(a)

    # returns
    G = []
    acc = 0.0
    for r in reversed(rewards):
        acc = r.item() + GAMMA * acc
        G.append(acc)
    G = list(reversed(G))
    G_t = torch.tensor(G, dtype=torch.float32)

    gstack = torch.stack(gbuf)

    with torch.no_grad():
        baseline = critic(gstack)
    adv = G_t - baseline
    adv = (adv - adv.mean()) / (adv.std() + 1e-6)

    diag = {
        "served_total": int(sum(served_counts)),
        "avg_reward": float(torch.mean(torch.stack(rewards)).item()),
        "mode_hist": {CANDIDATE_MODES[i]["name"]: int(sum(1 for x in chosen_modes if x == i)) for i in range(N_MODES)}
    }

    return G_t, adv.detach(), torch.stack(logps), torch.stack(ents), gstack, diag


# ============================================================
# Eval (deterministic policy)
# ============================================================
def run_episode_optionA_eval(coords, tt, policy: CandidatePolicyNet):
    st = make_initial_state(coords, tt)
    pending: List[Request] = []
    total_served = 0

    for _ in range(EP_STEPS):
        for _ in range(REQUESTS_PER_EPOCH):
            pending.append(generate_request(st))
        pending = [r for r in pending if r.deadline >= st.t]

        g = torch.tensor(global_features(st, pending), dtype=torch.float32)
        a = policy.deterministic(g)

        cands = generate_candidates(st, pending)
        chosen = cands[a]
        total_served += len(chosen["served"])
        apply_action_and_advance(st, pending, chosen["action"], chosen["served"])

    return total_served


