def train_single_seed(seed: int, outdir=OUTDIR, log_curve=True):
    random.seed(seed); np.random.seed(seed); torch.manual_seed(seed)

    coords, tt = make_grid(GRID_SIDE, AREA)
    policy = CandidatePolicyNet(d_in=9, h=64, n_actions=N_MODES)
    critic = Critic(d_in=9, h=64)

    opt_p = torch.optim.Adam(policy.parameters(), lr=LR)
    opt_c = torch.optim.Adam(critic.parameters(), lr=LR)

    ent_coef = ENT0
    ep_served = []
    snap_evals = []
    best_eval = -1e9
    best_state = None
    diag_rows = []

    for ep in range(1, TRAIN_EPISODES + 1):
        G_t, adv, logps, ents, gstack, diag = run_episode_optionA(coords, tt, policy, critic, ent_coef=ent_coef, debug=False)
        ep_served.append(diag["served_total"])

        # critic update
        pred = critic(gstack)
        v_loss = F.mse_loss(pred, G_t.detach())
        opt_c.zero_grad()
        v_loss.backward()
        nn.utils.clip_grad_norm_(critic.parameters(), GRAD_CLIP)
        opt_c.step()

        # policy update
        pg_loss = -(adv * logps).sum() - ent_coef * ents.sum()
        opt_p.zero_grad()
        pg_loss.backward()
        nn.utils.clip_grad_norm_(policy.parameters(), GRAD_CLIP)
        opt_p.step()

        ent_coef *= ENT_DECAY

        diag_rows.append({
            "episode": ep,
            "served": diag["served_total"],
            "avg_reward": diag["avg_reward"],
            **{f"mode_{k}": v for k, v in diag["mode_hist"].items()}
        })

        if log_curve and (ep % 100) == 0:
            mh = ", ".join([f"{k}:{v}" for k, v in diag["mode_hist"].items()])
            print(f"[train s{seed}] ep={ep:04d} served={diag['served_total']:4d} avgR={diag['avg_reward']:.2f} modes({mh})")

        if (ep % EVAL_EVERY) == 0:
            snap = float(np.mean([run_episode_optionA_eval(coords, tt, policy) for _ in range(SNAP_EVAL_EPISODES)]))
            snap_evals.append((ep, snap))
            if snap > best_eval:
                best_eval = snap
                best_state = {k: v.cpu().clone() for k, v in policy.state_dict().items()}
            print(f"[train s{seed}] ep={ep:04d} snap_eval={snap:.2f} best={best_eval:.2f}")

    if best_state is not None:
        policy.load_state_dict(best_state)
        print(f"[seed {seed}] restored best snapshot eval≈{best_eval:.2f}")

    pg_eval = float(np.mean([run_episode_optionA_eval(coords, tt, policy) for _ in range(EVAL_EPISODES)]))
    nomod_eval = float(np.mean([run_episode_nomod(coords, tt) for _ in range(EVAL_EPISODES)]))
    heur_eval = float(np.mean([run_episode_heur(coords, tt) for _ in range(EVAL_EPISODES)]))

    Path(outdir).mkdir(exist_ok=True)

    with open(os.path.join(outdir, f"curve_seed{seed}.csv"), "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["episode", "served"])
        for i, v in enumerate(ep_served, 1):
            w.writerow([i, v])

    with open(os.path.join(outdir, f"diag_seed{seed}.csv"), "w", newline="") as f:
        cols = list(diag_rows[0].keys()) if diag_rows else ["episode", "served", "avg_reward"]
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        w.writerows(diag_rows)

    return ep_served, snap_evals, pg_eval, nomod_eval, heur_eval


def run_experiment(seed_list=(1,2,4,8)):
    curves = []
    snap_eval_curves = []
    rows = []

