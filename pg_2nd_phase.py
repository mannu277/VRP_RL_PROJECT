def apply_action_and_advance(state: State, pending: List[Request], action: Action, served: List[int]):
    if action:
        for v_id, route in action.items():
            v = state.vehicles[v_id]
            t = v.time
            node = v.node
            for (i, j) in route:
                t += state.tt[i, j] + SERVICE_TIME
                node = j
            v.time = t
            v.node = node

    for idx in sorted(served, reverse=True):
        pending.pop(idx)

    state.t += DT


def make_initial_state(coords, tt, n_vehicles=N_VEH):
    vehicles = []
    N = len(coords)
    for i in range(n_vehicles):
        if i == 0:
            node = 0
        elif i == 1:
            node = N - 1
        else:
            node = int((i / n_vehicles) * N)
        vehicles.append(Vehicle(id=i, capacity=VEH_CAP, time=0.0, node=node))
    return State(T=10.0, t=0.0, tt=tt, coords=coords, vehicles=vehicles)


