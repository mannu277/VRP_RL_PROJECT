def run_episode_nomod(coords, tt):
    st = make_initial_state(coords, tt)
    pending: List[Request] = []
    tot = 0
    for _ in range(EP_STEPS):
        for _ in range(REQUESTS_PER_EPOCH):
            pending.append(generate_request(st))
        pending = [r for r in pending if r.deadline >= st.t]
        K = len(pending); V = len(st.vehicles)
        w = [1.0]*K; b = [0.0]*K; dq = [0.0]*V
        action, served, _ = solve_epoch_milp_modified(st, pending, w, dq, b, TIME_LIMIT_MIP)
        tot += len(served)
        apply_action_and_advance(st, pending, action, served)
    return tot

def run_episode_heur(coords, tt):
    st = make_initial_state(coords, tt)
    pending: List[Request] = []
    tot = 0
    for _ in range(EP_STEPS):
        for _ in range(REQUESTS_PER_EPOCH):
            pending.append(generate_request(st))
        pending = [r for r in pending if r.deadline >= st.t]

        served_indices = []
        used = set()
        for req_idx, req in enumerate(pending):
            best_v = None
            best_time = float("inf")
            for v in st.vehicles:
                if v.id in used:
                    continue
                if v.time > st.t:
                    continue
                if req.pax > v.capacity:
                    continue
                arr_p = v.time + st.tt[v.node, req.pickup] + SERVICE_TIME
                arr_d = arr_p + st.tt[req.pickup, req.drop] + SERVICE_TIME
                if arr_d <= req.deadline and arr_d < best_time:
                    best_time = arr_d
                    best_v = v
            if best_v is not None:
                best_v.time = best_time
                best_v.node = req.drop
                used.add(best_v.id)
                served_indices.append(req_idx)

        tot += len(served_indices)
        for idx in sorted(served_indices, reverse=True):
            pending.pop(idx)
        st.t += DT
    return tot


